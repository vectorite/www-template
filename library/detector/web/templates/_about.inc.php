<h3 style="margin-top: -11px;">About Detector</h3>
<p>
	<a href="https://github.com/dmolsen/Detector">Detector</a> is a simple, PHP- and JavaScript-based browser- and feature-detection library that can adapt to new devices &amp; browsers on its own without the need
	to pull from a central database of browser information.
</p>
<p>	
    Detector dynamically creates profiles using a browser's <em>(mainly)</em> unique user-agent string as a key. Using <a href="http://modernizr.com/">Modernizr</a> it records the HTML5 &amp; CSS3 features a requesting browser may or may not support. <a href="https://github.com/dmolsen/ua-parser-php">ua-parser-php</a> is used to collect and record any useful information <em>(like OS or device name)</em> the user-agent string may contain. 
</p>
<p> 
    With Detector a developer 
	can serve the appropriate markup, stylesheets, and JavaScript to a requesting browser without being completely dependent on a front-end-only resource loader nor a browser-detection library being up-to-date.
</p>

<p>
	<a href="https://github.com/dmolsen/Detector">Check out the README</a> for more information as well as the <a href="https://github.com/dmolsen/Detector">full list of features</a>.
</p>